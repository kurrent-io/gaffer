// Syncs the canonical telemetry notices into the docs content collection.
//
// The notice for each surface lives next to the code that emits it
// (cli/TELEMETRY.md, editors/vscode/TELEMETRY.md) and is the primary
// public telemetry contract for that surface. This script renders each
// into a Starlight page under src/content/docs/telemetry/ and generates
// an index that lists the surfaces. The gaffer binary and the telemetry
// worker consume the same source files, so there is one source of truth.
//
// Output is gitignored and regenerated before every dev/build/check run.
// Adding a new surface is a one-line edit to `surfaces` below.

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const docsRoot = resolve(here, "..");
const repoRoot = resolve(docsRoot, "..");
const outDir = resolve(docsRoot, "src/content/docs/telemetry");

const surfaces = [
	{
		slug: "cli",
		title: "Gaffer CLI telemetry",
		linkLabel: "Gaffer CLI",
		description: "What the gaffer CLI collects, how to opt out, and how to delete your data.",
		src: "cli/TELEMETRY.md",
	},
	{
		slug: "vs-code",
		title: "VS Code extension telemetry",
		linkLabel: "VS Code extension",
		description: "What the gaffer VS Code extension collects, how to opt out, and how to delete your data.",
		src: "editors/vscode/TELEMETRY.md",
		// The standalone file links to cli/TELEMETRY.md on GitHub so it
		// reads on its own; point it at the sibling docs page instead.
		rewrites: [["https://github.com/kurrent-io/gaffer/tree/main/cli/TELEMETRY.md", "./cli.md"]],
	},
];

const banner = (src) => `<!-- Generated by scripts/sync-telemetry-notices.mjs from ${src}. Do not edit by hand. -->`;

function frontmatter(title, description) {
	return `---\ntitle: ${title}\ndescription: ${description}\n---`;
}

function renderSurface(surface) {
	let md = readFileSync(resolve(repoRoot, surface.src), "utf8");
	for (const [from, to] of surface.rewrites ?? []) {
		md = md.replaceAll(from, to);
	}
	// Drop the leading H1; Starlight renders the title from frontmatter.
	md = md.replace(/^#\s+.*\r?\n+/, "");
	return `${frontmatter(surface.title, surface.description)}\n${banner(surface.src)}\n\n${md.trimStart()}`;
}

function renderIndex() {
	const intro =
		"Gaffer emits anonymous usage telemetry to help Kurrent prioritise features and fix bugs. " +
		"Nothing that could identify you or your projection code is collected, and every surface " +
		"can be turned off. Each surface is governed independently by its own opt-outs.";
	const list = surfaces.map((s) => `- [${s.linkLabel}](./${s.slug}.md): ${s.description}`).join("\n");
	const body = `${intro}\n\n${list}\n`;
	return `${frontmatter("Telemetry", "What gaffer collects, why, and how to opt out.")}\n${banner("scripts/sync-telemetry-notices.mjs")}\n\n${body}`;
}

mkdirSync(outDir, { recursive: true });
for (const surface of surfaces) {
	writeFileSync(resolve(outDir, `${surface.slug}.md`), renderSurface(surface));
}
writeFileSync(resolve(outDir, "index.md"), renderIndex());

console.log(`synced telemetry notices -> src/content/docs/telemetry/ (${surfaces.map((s) => s.slug).join(", ")} + index)`);
