// Syncs the diagnostics reference page from the catalog artifact.
//
// cli/internal/mcpserver/resources/diagnostics.gen.json is the single source
// of truth - generated from the C# DiagnosticCatalog and drift-guarded by a
// runtime test. This script renders it into a Starlight page at
// src/content/docs/reference/diagnostics.md, one entry per code, with a stable
// per-code anchor (`<a id="<code>">`) so the CLI and editor can deep-link to
// `#<code>` without depending on Starlight's lossy heading slugs.
//
// Output is gitignored and regenerated before every dev/build/check run.

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const docsRoot = resolve(here, "..");
const repoRoot = resolve(docsRoot, "..");
const artifact = resolve(repoRoot, "cli/internal/mcpserver/resources/diagnostics.gen.json");
const outDir = resolve(docsRoot, "src/content/docs/reference");
const outFile = resolve(outDir, "diagnostics.md");

const banner =
	"<!-- Generated by scripts/sync-diagnostics.mjs from cli/internal/mcpserver/resources/diagnostics.gen.json. Do not edit by hand. -->";

function frontmatter(title, description) {
	// JSON.stringify quotes the values as valid YAML double-quoted scalars.
	return `---\ntitle: ${JSON.stringify(title)}\ndescription: ${JSON.stringify(description)}\n---`;
}

const severityLabel = { error: "Error", warning: "Warning", information: "Information" };

function renderEntry(d) {
	const meta = [`**Severity:** ${severityLabel[d.severity] ?? d.severity}`];
	if (d.class === "quirk") {
		meta.push(d.fixedIn ? `**Fixed in:** KurrentDB ${d.fixedIn}` : "**Fixed in:** _not yet shipped upstream_");
	}
	// The code-verbatim anchor is the deep-link target; the heading keeps its
	// own (lossy) Starlight slug for the table of contents.
	const parts = [
		`<a id=${JSON.stringify(d.code)}></a>`,
		"",
		`### \`${d.code}\``,
		"",
		meta.join(" · "),
		"",
		(d.docs || d.message || "").trim(),
		"",
	];
	if (d.badExample) parts.push("**Problem**", "", "```js", d.badExample.trim(), "```", "");
	if (d.goodExample) parts.push("**Fix**", "", "```js", d.goodExample.trim(), "```", "");
	return parts.join("\n");
}

function renderSection(title, intro, entries) {
	if (entries.length === 0) return "";
	return [`## ${title}`, "", intro, "", ...entries.map(renderEntry)].join("\n");
}

const all = JSON.parse(readFileSync(artifact, "utf8"));
const quirks = all.filter((d) => d.class === "quirk");
const usage = all.filter((d) => d.class === "usage");

const intro =
	"Gaffer surfaces diagnostics while validating and running projections: they appear on the " +
	"`gaffer dev` summary, in your editor's Problems panel, and on `FeedResult.diagnostics` in the " +
	"testing library. Every diagnostic has a stable `<class>.<subject>.<detail>` code.\n\n" +
	"- **`quirk.*`** reproduces a KurrentDB engine bug so local runs match production. A quirk fires " +
	"whenever `quirks_version` is unset (the default, which reproduces every known quirk), or is set " +
	"to a release earlier than its **Fixed in** version. Targeting that release or later turns it off.\n" +
	"- **`usage.*`** flags something about your own projection code: an unsupported construct, a no-op " +
	"under an engine version, or a deprecation.\n\n" +
	"Severity is **Error** (no correct form: it throws or is unsupported), **Warning** (runs but " +
	"produces a wrong or surprising result), or **Information** (works, but noteworthy).";

const body = [
	renderSection("Quirks", "Reproduced KurrentDB engine bugs, gated by `quirks_version`.", quirks),
	renderSection("Usage", "Issues in your own projection code.", usage),
]
	.filter(Boolean)
	.join("\n");

const page = `${frontmatter("Diagnostics", "Every gaffer diagnostic code: what each quirk.* and usage.* means and how to act on it.")}\n${banner}\n\n${intro}\n\n${body}\n`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, page);

console.log(`synced diagnostics reference -> src/content/docs/reference/diagnostics.md (${all.length} codes)`);
